// 返回 { score, label }；label: 'bull' 利好 / 'bear' 利空 / 'neutral' 中性
export function scoreNews(text) {
  if (!text) return { score: 0, label: 'neutral' };
  let s = 0;
  for (const w in POS) if (text.includes(w)) s += POS[w];
  for (const w in NEG) if (text.includes(w)) s -= NEG[w];
  s = Math.round(s * 10) / 10;
  const label = s >= 1.0 ? 'bull' : (s <= -1.0 ? 'bear' : 'neutral');
  return { score: s, label };
}
