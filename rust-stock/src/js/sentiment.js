  '涨停板': 3.0, '涨停': 3.0, '牛市': 3.0, '暴涨': 3.0, '飙升': 2.6, '大涨': 2.5, '创新高': 2.8, '新高': 2.5,
  '超预期': 2.5, '利好': 2.5, '拉升': 2.2, '回购': 2.2, '扭亏': 2.2, '突破': 2.0, '上涨': 2.0, '走高': 2.0,
